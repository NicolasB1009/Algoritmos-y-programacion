Mujeres=int(input("Ingrese el número de mujeres: "))
Hombres=int(input("Ingrese el número de hombres: "))
Total_estudiantes=Mujeres+Hombres
Porcentaje_mujeres=((Mujeres*100)/Total_estudiantes)
Porcentaje_hombres=((Hombres*100)/Total_estudiantes)

print("El porcentaje de mujeres es: ",Porcentaje_mujeres,"%","El porcentaje de hombres es: ",Porcentaje_hombres,"%")